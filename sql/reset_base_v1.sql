begin
  if not exists (select 1 from pg_policies where tablename='game_config' and policyname='p_game_config_all') then
    create policy p_game_config_all on public.game_config for all using (true) with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='game_items' and policyname='p_game_items_all') then
    create policy p_game_items_all on public.game_items for all using (true) with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='game_tiles' and policyname='p_game_tiles_all') then
