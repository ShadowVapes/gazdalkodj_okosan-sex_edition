    create policy p_game_cards_all on public.game_cards for all using (true) with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='rooms' and policyname='p_rooms_all') then
    create policy p_rooms_all on public.rooms for all using (true) with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='room_players' and policyname='p_room_players_all') then
    create policy p_room_players_all on public.room_players for all using (true) with check (true);
  end if;
end $$;
